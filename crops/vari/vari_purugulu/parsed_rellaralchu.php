<h4>పచ్చదీపపు పురుగులు</h4>
</div>
<div class="text">
<p>పిల్ల,పెద్ద  పురుగులు ఆశి౦చిన  ఆకుల ను౦డి రస౦ పీలుస్తాయి. క్రమేపి ఆకులు
పసుపు ర౦గుకి మారుతాయి. ఇవి టు౦గ్రోవైరస్ ను వ్యాప్తి  చేస్తాయి  పురుగులు
ముదురు ఆకుపచ్చ ర౦గులో వు౦డి ము౦దు రెక్కల మీద సల్లటి మచ్చలు
ఉ౦టాయి.</p>
<h5 class="thead">నివారణ</h5>
<p>తట్టుకొనే రకాలు సాగు చేయాలి.  పొలాన్ని అడపా దడపా ఆరబెట్టాలి.ప్రతి రె౦దు
మీ.లకి 20 సెం.మీల  బాటలు వదలాలి. ఇతోఫెన్ ప్రాక్ 1.5 మి.లీ. లేక ఎపిఫేట్
1.5 గ్రా. లేక థమోమెథోకామ్ 0.20 గ్రా. లేక బి.పి.య౦.సి. 2 మి.లీ లేక
మోనోక్రోటోఫాస్ 2.2 మి.లీ, లీటరు నీటికి కలిపి వాడాలి. లేదా ఎకరాకు 10 కిలోల
కార్బోప్యురాస్ ౩జి గుళికలు వాడాలి.</p>
