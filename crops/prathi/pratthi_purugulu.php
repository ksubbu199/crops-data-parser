ఎండోసల్ఫాన్(2మి.లీ నీటికి) మరియు ఎన్.పి.వైరస్ ద్రావణం (500యల్.ఇ/హెక్టారుకి)టీపాలు,బెల్లంతో కలిపి పిచికారి చేయాలి(విత్తిన 100-110రోజులకు).తర్వాత 5% వేపగింజల ద్రావణం(విత్తిన 125వ రోజు)మరియు క్లోరోఫైరిపాస్(2మి.లీ లీటరు నీటికి)5% నువ్వుల నూనెతో కలిపి (విత్తిన 135-140వ రోజు)పిచికారి చేసి ఈ పురుగు ఉధృతిని అదుపులో పెట్టవచ్చు.

