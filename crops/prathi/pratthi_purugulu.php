పురుగు తాకిన కాయలు పూర్తిగా వృద్ది చెందక త్వరగా పక్వానికి వస్తాయి.లోపలి నుండి విత్తనాలను తిని నష్టపరచడం వలన దూది నాణ్యత తగ్గిపోతుంది.

