		<h4>
           విత్తే సమయం
        </h4>
        <p>
          నీటి పారుదల పంటగా సంవత్సర౦ పొడవున పండించవచ్చు.ప్రొద్దుతిరుగుడు విత్తనం విట్టేతప్పుడు గుర్తుంచుకోవలసిన ముఖ్యమైన విషయమేమంటే పూత దశ మరియు గింజలు తయారయ్యే దశలో పంట దీర్ఘకాల వర్షంతోగాని లేదా పగటి ఉష్ణోగ్రత 38సేల్సియన్ కంటె ఎక్కువగా గాని ఉండకుండా చూసుకోవాల్సి వుంటుంది.ఖరీఫ్ లో తేలిక పాటి నేలల్లో జూన్ రెండవ పక్షం నుండి జూలై 15వ తేది వరకు ,బరువు నేలల్లో ఆగుష్టు రెండవ పక్షంలో విట్టుకోవచ్చు.రబీ వర్షాధార౦ కింద నవంబర్ లో,నీటి పారుదల కింద జనవరి రెండవ పక్షం నుండి ఫిబ్రవరి మొదటి వారం వరకు విత్తుకోవచ్చు.నల్ల రేగడి నేలల్లో తొందరగా నాతుకున్నట్లితే దిగుబడి బాగా వస్తుంది.
ఎర్ర నేలలు మరియు నల్ల రేగడి నేలల్లో వారి తరువాత ప్రోద్దుతిరుగుడు వేసుకొనే పక్షం డిసెంబర్ ఆఖరి వారం నుండి జనవరి మొదటి వారం వరకు విత్తుకోవాలి.విత్తే సమయం కూడా ప్రొద్దుతిరుగుడు యొక్క నూనె నాణ్యతను పెంచుతుంది.పువ్వు వికసించే మరియు విత్తనం గట్టి పడే సమయంలో ఎక్కువగా పగలు (12గం.లు) మరియు సూర్య రశ్మి ఉన్నట్లయితే నూనె  శాతం ఎక్కువగా వస్తుంది. నూనె నిల్వ సామర్ధ్యం ఖరీఫ్ కంటె వేసవిలో ఎక్కువగా వుంటుంది.వేసవి పంట లో ఒలిక్యాసిడ్ నూనె శాతం ఎక్కువగా వుంటుంది.నీటి పారుదల ఉన్నట్లయితే ఉష్నోగ్రతల నిమిత్తం లేకుండా ప్రొద్దుతిరుగుడు సంవత్సరం పొడవునా పండించవచ్చు అయితే రబీ మరియు వేసవిలో విత్తిన పంట ఖరీఫ్ పంట కంటె ఎక్కువగా దిగుబడి నిస్తుంది. 
        </p>
   <br>
        
		<h4>
          విత్తన మోతాదు
        </h4>
        <p>
          వర్షాధారంగా పండిస్తే ఎకరాకు రకాలను 3-4కిలోలు ,సంకరాల కైతే 2-2.5కిలోల విత్తనం కావలి.నీటి పారుదల క్రింద ఎకరాకు 2.5-3.5కిలోల మరియు సంకరాలకు 2కిలోల విత్తనం సరిపోతుంది.

		</p>
   <br>
        
		<h4>
         విత్తన శుద్ధి
        </h4>
        <p>
          విత్తనాలను విత్తేముందు 14గంటలు నీటిలో నానబెట్టి తరువాత నీడలో ఆరబెట్టి విత్తనా శుధ్ధి చేయాలి.విత్తే ముందు కిలో విత్తనానికి 2-3గ్రాముల థైరమ్ లేదా కాప్తాన్ కలిపి విత్తనా శుద్ధి చేయాలి.నేక్రోసిన్ తీవ్రంగా ఉన్న ప్రాంతాల్లో కిలో విత్తనానికి 5గ్రా ఇమిడాక్లోప్రిడ్ కలిపి విత్తన శుద్ధి చేయాలి.

		</p>
   <br>
        
		<h4>
         విత్తేదూరం
        </h4>
        <p>
          తేలిక నెలల్లో అంతరం 40x2200 సెం.మి మొక్కల సాంద్రత ఎకరాకు 44,400 ఉండాలి.మధ్యస్థ (పొట్టి /తక్కువగా కాలపరిమితి రకాలు)నేలల్లో అంతరం ఎకరాకు  45x30సెం.మి మొక్కల సాంద్రత  29600ఎకరాకు ఉండాలి.బరువు (పొడవు/దీర్ఘ కాలపరిమితి రకాలు సంకరాలు )నేలల్లో అంతరం ఎకరాకు 60x30సెం.మీ మొక్కల  సాంద్రత  22000 ఎకరాకు.
విత్తనాన్ని 2-3సెం.మీ లోతులో నాతవలసిన ఉంటుంది.మొక్కలు మొలకెత్తిన 10-15 రోజుల తర్వాత విధిగా కుదురుకు ఆరోగ్యవంతమైన ఒకే  మొక్కనుంచాలి.

